
#Define the functions

def Add(intNum_1,intNum_2):
    return intNum_1 + intNum_2

def Double(intNum):
    return 2*intNum


#Initialize the integer values

intNum_1 = 5

intNum_2 = 10

#Perform the Operations
    
Sum = Add(intNum_1,intNum_2)
        
Doubled_Sum = Double(Sum)

print(Doubled_Sum)
